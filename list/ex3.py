# Bài 3: Theo dõi điểm số
# scores = [85, 92, 78, 90, 88]
# Yêu cầu

# Tính:

# Tổng điểm
# Điểm cao nhất
# Điểm thấp nhất
# Điểm trung bình

# Gợi ý: sum, min, max

scores = [8, 10, 12, 15, 49]

print("Tổng điểm:", sum(scores))

print("Điểm cao nhất:", max(scores))

print("Điểm thấp nhất:", min(scores))

print("Điểm trung bình:", sum(scores) / len(scores))